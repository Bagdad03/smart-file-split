namespace SmartFileSplit.Core;

/// <summary>
/// Потоково отдаёт строки одной входной таблицы как массивы строк.
/// Своя реализация на каждый тип входа (CSV, Excel). Реализует IDisposable,
/// потому что во время перечисления строк файл/поток держится открытым.
/// </summary>
public interface ITableReader : IDisposable
{
    /// <summary>
    /// Лениво перечисляет все строки таблицы, включая первую (заголовочную).
    /// Каждая строка — это сырые значения ячеек как строки; пустая ячейка — <c>""</c>.
    /// </summary>
    IEnumerable<string[]> ReadRows();
}